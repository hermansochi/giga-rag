"""
Модуль, определяющий базовый интерфейс для всех парсеров документов.

Содержит абстрактный класс BaseDocumentParser, который служит основой
для всех конкретных парсеров (TXT, PDF, CSV, JSON и др.).

Цель модуля — обеспечить единый интерфейс для обработки различных
типов документов, что позволяет системе RAG (Retrieval-Augmented Generation)
единообразно работать с разными форматами файлов.
"""

from abc import ABC, abstractmethod
from typing import List, Tuple, Dict, Any
from pathlib import Path


class BaseDocumentParser(ABC):
    """
    Абстрактный базовый класс для парсеров документов.

    Определяет общий интерфейс, который должны реализовывать все
    конкретные парсеры (например, TXTParser, PDFParser, CSVParser и др.).

    Каждый парсер отвечает за преобразование бинарного содержимого
    документа в список текстовых фрагментов (страниц), пригодных
    для последующей обработки (чанкинг, индексация, поиск).

    Основной метод parse() возвращает список кортежей, где каждый
    кортеж содержит номер фрагмента (условная "страница") и его текст.
    Это позволяет сохранить структуру документа при его разбиении на чанки.

    """

    @abstractmethod
    def parse(self, file_bytes: bytes, filename: str) -> List[Tuple[int, str]]:
        """
        Основной абстрактный метод для парсинга документа.

        Должен быть реализован в каждом конкретном парсере.
        Преобразует бинарные данные файла в структурированный текстовый формат.

        Args:
            file_bytes (bytes): Бинарные данные файла для парсинга.
            filename (str): Имя файла (используется для логирования и определения типа).

        Returns:
            List[Tuple[int, str]]: Список кортежей (номер_фрагмента, текст_фрагмента).

        Notes:
            - Для документов без естественной страницы (например, TXT) номер всегда 1.
            - Для многостраничных документов (PDF) номер соответствует номеру страницы.
            - Для табличных данных (CSV) каждая строка может быть отдельным фрагментом.
            - Пустые или невалидные документы должны возвращать пустой список.
        """
        pass


    def get_supported_extensions(self) -> List[str]:
        """
        Возвращает список поддерживаемых расширений файлов.

        Каждое расширение возвращается в нижнем регистре с точкой (например, '.pdf').

        Этот метод может быть переопределён в подклассах для указания
        конкретных поддерживаемых форматов.

        Returns:
            List[str]: Список строк с поддерживаемыми расширениями.
            По умолчанию возвращает пустой список.

        Example:
            Для PDF парсера: ['.pdf']
            Для TXT парсера: ['.txt', '.text', '.md']
        """
        return []


    @staticmethod
    def get_file_extension(filename: str) -> str:
        """
        Возвращает расширение файла в нижнем регистре с точкой.

        Утилитарный статический метод для извлечения расширения файла.
        Используется парсерами для определения типа документа по имени файла.

        Args:
            filename (str): Имя файла (может включать путь).

        Returns:
            str: Расширение файла в нижнем регистре с точкой (например, '.pdf').
                 Если расширение отсутствует, возвращает пустую строку.

        Example:
            >>> BaseDocumentParser.get_file_extension("document.pdf")
            '.pdf'
            >>> BaseDocumentParser.get_file_extension("data.csv.gz")
            '.gz'
            >>> BaseDocumentParser.get_file_extension("image")
            ''
        """
        return Path(filename).suffix.lower()