"""
Модуль для парсинга PDF-файлов с использованием библиотеки PyMuPDF (fitz).

Содержит класс PDFParser, реализующий интерфейс BaseDocumentParser,
и вспомогательную функцию parse_pdf().

Класс PDFParser предназначен для извлечения текстового содержимого из PDF-документов
с сохранением структуры по страницам. Он корректно обрабатывает многостраничные
документы и пропускает пустые или почти пустые страницы.
"""

import streamlit as st
from typing import List, Tuple
import fitz  # PyMuPDF
from ..base import BaseDocumentParser


class PDFParser(BaseDocumentParser):
    """
    Парсер для PDF-документов.

    Данный класс отвечает за открытие PDF-файлов из байтового потока,
    извлечение текста со всех страниц и возврат его в структурированном виде.
    Поддерживает многостраничные документы и сохраняет номера страниц.

    При извлечении текста применяется фильтрация: страницы с менее чем 10 символами
    считаются пустыми и не включаются в результат. Это помогает избежать
    добавления служебных страниц (например, пустых листов оглавления).

    Поддерживаемые расширения файлов:
        - .pdf

    Attributes:
        None (нет внутреннего состояния)

    Methods:
        get_supported_extensions() -> List[str]:
            Возвращает список поддерживаемых расширений файлов.

        parse(file_bytes: bytes, filename: str) -> List[Tuple[int, str]]:
            Основной метод парсинга. Принимает бинарные данные PDF-файла
            и его имя. Возвращает список кортежей, где каждый кортеж содержит
            номер страницы (начиная с 1) и её текстовое содержимое.
            Возвращает пустой список в случае ошибки или если не удалось
            извлечь текст из страниц.
    """

    def get_supported_extensions(self) -> List[str]:
        """
        Возвращает список расширений файлов, поддерживаемых этим парсером.

        Returns:
            List[str]: Список строк с расширениями, в данном случае ['.pdf'].
        """
        return [".pdf"]

    def parse(self, file_bytes: bytes, filename: str) -> List[Tuple[int, str]]:
        """
        Парсит бинарное содержимое PDF-файла и извлекает текст со всех страниц.

        Метод открывает PDF-документ из байтового потока с помощью библиотеки PyMuPDF (fitz),
        последовательно обрабатывает каждую страницу, извлекает текст в виде простого текста
        (без сохранения форматирования) и сохраняет его вместе с номером страницы.

        Каждая страница обрабатывается следующим образом:
        1. Извлекается текст с помощью метода get_text("text").
        2. Текст очищается от пробелов в начале и конце (strip()).
        3. Проверяется, что текст не пустой и содержит более 10 символов.

        Пустые и служебные страницы (например, пустые листы или короткие номера страниц)
        автоматически пропускаются, чтобы не засорять результат.

        Args:
            file_bytes (bytes): Бинарные данные PDF-файла для парсинга.
            filename (str): Имя файла (используется для логирования ошибок).

        Returns:
            List[Tuple[int, str]]: Список кортежей (номер_страницы, текст_страницы),
            где номера страниц начинаются с 1. Возвращает пустой список,
            если документ пустой, содержит ошибки или не удалось извлечь текст.

        Example:
            >>> with open("document.pdf", "rb") as f:
            ...     content = f.read()
            >>> pages = PDFParser().parse(content, "document.pdf")
            >>> for page_num, text in pages:
            ...     print(f"Страница {page_num}:\n{text}\n---\n")
        """
        pages = []

        try:
            doc = fitz.open(stream=file_bytes, filetype="pdf")

            for page_num in range(len(doc)):
                page = doc[page_num]
                text = page.get_text("text").strip()

                # Пропускаем пустые страницы
                if text and len(text) > 10:
                    pages.append((page_num + 1, text))  # нумерация страниц с 1

            doc.close()

            if not pages:
                st.error(f"⚠️ В PDF {filename} не удалось извлечь текст")

            return pages

        except Exception as e:
            st.error(f"❌ Ошибка парсинга PDF {filename}: {e}")
            return []


# Для удобного импорта
def parse_pdf(file_bytes: bytes, filename: str) -> List[Tuple[int, str]]:
    """
    Удобная обёртка для парсинга PDF-файлов.

    Создаёт экземпляр PDFParser и вызывает его метод parse().

    Эта функция предоставляет простой интерфейс для использования
    парсера без необходимости явного создания объекта.

    Предназначена для импорта и использования в других модулях проекта.

    Args:
        file_bytes (bytes): Бинарные данные файла.
        filename (str): Имя файла для логирования.

    Returns:
        List[Tuple[int, str]]: Результат парсинга — список текстовых страниц
        или пустой список при ошибке.

    Example:
        >>> with open("document.pdf", "rb") as f:
        ...     content = f.read()
        >>> pages = parse_pdf(content, "document.pdf")
        >>> if pages:
        ...     first_page_text = pages[0][1]
    """
    parser = PDFParser()
    return parser.parse(file_bytes, filename)
